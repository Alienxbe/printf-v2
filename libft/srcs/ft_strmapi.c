#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*new;
	size_t	i;

	if (!s || !f)
		return (NULL);
	new = ft_strdup(s);
	i = -1;
	while (new && s[++i])
		new[i] = f(i, s[i]);
	return (new);
}
